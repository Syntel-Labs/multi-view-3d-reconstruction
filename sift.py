import cv2 as cv


def sift_pipeline(frontal, side, ratio=0.75):
    """
    Pipeline SIFT para detección y matching de features.

    Args:
        frontal: imagen 1
        side: imagen 2
        ratio: umbral para Lowe's ratio test

    Returns:
        Imagen con matches dibujados
    """

    sift = cv.SIFT_create()

    kp1, des1 = sift.detectAndCompute(frontal, None)
    kp2, des2 = sift.detectAndCompute(side, None)

    if des1 is None or des2 is None:
        raise ValueError("No se pudieron detectar descriptores en alguna imagen")

    bf = cv.BFMatcher(cv.NORM_L2, crossCheck=False)
    matches = bf.knnMatch(des1, des2, k=2)

    good = []
    for m, n in matches:
        if m.distance < ratio * n.distance:
            good.append([m])

    matched_image = cv.drawMatchesKnn(
        frontal,
        kp1,
        side,
        kp2,
        good,
        None,
        flags=cv.DrawMatchesFlags_NOT_DRAW_SINGLE_POINTS
    )

    return matched_image
